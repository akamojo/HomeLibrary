package com.vaadin;

import java.io.Serializable;

public class Comment implements Serializable, Cloneable {
	private String user = "";
	private String text = "";
	private Long id;
	
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
	public boolean isPersisted() {
		return id != null;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (this.id == null) {
			return false;
		}

		if (obj instanceof Comment && obj.getClass().equals(getClass())) {
			return this.id.equals(((Comment) obj).id);
		}

		return false;
	}

	@Override
	public int hashCode() {
		int hash = 5;
		hash = 43 * hash + (id == null ? 0 : id.hashCode());
		return hash;
	}

	@Override
	public Comment clone() throws CloneNotSupportedException {
		return (Comment) super.clone();
	}


}
